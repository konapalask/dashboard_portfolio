// Function to simulate real-time price updates
export const simulatePriceUpdate = (stock: Stock): Stock => {
  const volatility = stock.beta * 0.005; // Use beta for realistic volatility
  const randomChange = (Math.random() - 0.5) * 2 * volatility;
  const newPrice = stock.currentPrice * (1 + randomChange);
  const dayChange = newPrice - (stock.currentPrice - stock.dayChange);
  const dayChangePercent = (dayChange / (newPrice - dayChange)) * 100;
  
  const totalValue = newPrice * stock.quantity;
  const totalInvested = stock.avgPrice * stock.quantity;
  const totalPnL = totalValue - totalInvested;
  const totalPnLPercent = (totalPnL / totalInvested) * 100;

  return {
    ...stock,
    currentPrice: newPrice,
    dayChange,
    dayChangePercent,
    totalValue,
    totalPnL,
    totalPnLPercent
  };
};